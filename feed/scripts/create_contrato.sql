DROP TABLE IF EXISTS contrato;

CREATE TABLE IF NOT EXISTS "contrato" ( 
    "CD_ORGAO" INTEGER,
    "NM_ORGAO" INTEGER,
    "NR_LICITACAO" INTEGER,
    "ANO_LICITACAO" INTEGER,
    "CD_TIPO_MODALIDADE" VARCHAR(3),
    "NR_CONTRATO" INTEGER,
    "ANO_CONTRATO" INTEGER,
    "TP_INSTRUMENTO" VARCHAR(1),
    "NR_PROCESSO" VARCHAR(20),
    "ANO_PROCESSO" INTEGER,
    "TP_DOCUMENTO" VARCHAR(1),
    "NR_DOCUMENTO" VARCHAR(14),
    "DT_INICIO_VIGENCIA" DATE,
    "DT_FINAL_VIGENCIA" DATE,
    "VL_CONTRATO" REAL,
    "DT_ASSINATURA" DATE,
    "BL_GARANTIA" VARCHAR(1),
    "NR_DIAS_PRAZO" INTEGER,
    "DS_OBJETO" VARCHAR(500),
    "NR_CONTRATO_ORIGINAL" VARCHAR(20),
    "BL_INICIO_DEPENDE_OI" VARCHAR(1),
    "DS_JUSTIFICATIVA" VARCHAR(300),
    "CNPJ_CONSORCIO" VARCHAR(14),
    "CNPJ_ORGAO_GERENCIADOR" VARCHAR(14),
    "BL_GERA_DESPESA" VARCHAR(1),
    "DS_OBSERVACAO" VARCHAR(500),
    FOREIGN KEY(CD_ORGAO, NM_ORGAO) REFERENCES licitacao ("CD_ORGAO", "NM_ORGAO"),
    PRIMARY KEY("NR_LICITACAO, ANO_LICITACAO, CD_TIPO_MODALIDADE, NR_CONTRATO, ANO_CONTRATO, TP_INSTRUMENTO")
);